    private void btn_bookNowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_bookNowActionPerformed
        // TODO add your handling code here:
        
        this.selectedSeats.clear();             //clear past selected seat records
        this.ticketsCount = 0;                  //reinitialize ticket for a reservation to 0
        this.totalCost = 0;                     //reinitialize total ticket cost for a reservation to 0
        this.tickets_txt.setText("");           //reset tickets
        this.seats_txt.setText("");             //reset seats
        this.totalCost_txt.setText("$" + Integer.toString(totalCost));  //reset total cost for a reservation
        this.selectedButtonList.forEach((btn) -> {                      //change colors of reserved seats to red
            btn.setBackground(new Color(158, 32, 40));
        });
        this.reservedSeats += selectedButtonList.size();                //total reserved seats
        if(reservedSeats == 84){                                        //cheack wether all seats are reserved
            JOptionPane.showMessageDialog(rootPane, "All seats are Reserved!!!");  //popup message to confirm reservations are disabled
            this.btn_bookNow.setEnabled(false);                                     //disable the reservation button
        }
        this.selectedButtonList.clear();                                            //clear records about buttons(seats)
    }//GEN-LAST:event_btn_bookNowActionPerformed

