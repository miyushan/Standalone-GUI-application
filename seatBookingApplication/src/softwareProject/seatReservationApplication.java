    /**
     * This method is used to control the application process with the calculations
     * @param evt 
     */
    private void btn_click(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_click
        // TODO add your handling code here:
        int seatCost;                                       //cost for a seat($12 or $20)
        String joinedNames = "";                            //Message to print in Seats area(Seats: )
        JButton clickedButton = (JButton)evt.getSource();   //object to access JButton data

        switch(clickedButton.getName()){                    //define seats cost considering its name
            case "R10C3":
            case "R10C4":
            case "R10C5":
            case "R10C6":
            case "R10C7":
            case "R10C8":
                seatCost = 20;      //last row seat cost
                break;
            default:
                seatCost = 12;      //upper row's seat cost
                break;
        };
        
        if(clickedButton.getBackground().equals(new Color(102, 102, 102))){     //check wether a button is not clicked earlier
            this.ticketsCount++;                                                //tickets for one reservation++
            clickedButton.setBackground(new Color(0, 142, 48));                 //change selected seat to green
            this.selectedButtonList.add(clickedButton);                         //store a new seat as selected
            this.totalCost += seatCost;                                         //total cost = previous cost + new seat cost
            selectedSeats.add(clickedButton.getName());                         //store seat name
        }

        else if(clickedButton.getBackground().equals(new Color(0, 142, 48))){   //check wether a button is clicked earlier
            this.ticketsCount--;                                                //tickets for one reservation++
            clickedButton.setBackground(new Color(102, 102, 102));              //change deselected seat to gray
            this.selectedButtonList.remove(clickedButton);                      //remove selected seat
            this.selectedSeats.remove(clickedButton.getName());                 //remove seat name
            this.totalCost -= seatCost;                                         //total cost = previous cost - removed seat cost
        }
        
        this.tickets_txt.setText(Integer.toString(this.ticketsCount));          //message in Tickets area
        
        this.totalCost_txt.setText("$" + Integer.toString(totalCost));          //message in Total area
        
        joinedNames = String.join("  ", selectedSeats);                         //Seat area's message as a string
        this.seats_txt.setText(joinedNames);                                    //message in Seats area
        
    }//GEN-LAST:event_btn_click

